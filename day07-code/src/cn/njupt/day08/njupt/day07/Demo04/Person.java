package cn.njupt.day08.njupt.day07.Demo04;

public class Person {

    private String name;
    private int age;

    public Person() {//code
    }

    public Person(String name, int age) {//code
        this.name = name;
        this.age = age;
    }

    public String getName() {//code
        return name;
    }

    public void setName(String name) {
        this.name = name;//code
    }

    public int getAge() {//code
        return age;
    }

    public void setAge(int age) {//code
        this.age = age;
    }
}
